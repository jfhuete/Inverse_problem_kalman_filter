function figure1 = PlotResults(samples, YMatrix1, vertex, plot_title, plot_values, legend_names, Parameters_Texts)

% Screen size to centrate plots
screen_size = get( groot, 'Screensize' );
width  = screen_size(3);
height = screen_size(4);
xpos = (width-1200)/2;
ypos = (height-768)/2;

% Axis values
max_value = max(max(YMatrix1(:,vertex,:)));
min_value = min(min(YMatrix1(:,vertex,:)));
YInterval = max_value-min_value;
YMargin = YInterval/2;

% Create figure
figure1 = figure;

set(figure1,'Position',[xpos, ypos, 1200, 768]);

% Create axes
axes1 = axes('Parent',figure1,'XTick',[samples],'XLim',[samples(1) samples(end)],...
    'Position',[0.365 0.100180491551459 0.59912884333822 0.821216888361205]);

hold(axes1,'all');
axis([min(samples), max(samples),min_value-YMargin,max_value+YMargin]);
% Create multiple lines using matrix input to plot
plot1 = plot(samples, squeeze(YMatrix1(:,vertex,:)));
set(plot1(1),'LineWidth',2.5,'Color',[1 0 0]);

% Create title
title([plot_title '. Node: ' num2str(vertex)],'FontWeight','bold','FontSize',18);

% Create xlabel
xlabel('Time instants');

% Create ylabel
ylabel(plot_values);

% Create legend
legend1 = legend(axes1,legend_names);
set(legend1,...
    'Position',[0.0450530362630917 0.535519039617721 0.278280297070242 0.386819165400198]);

% Create textbox
annotation(figure1,'textbox',...
    [0.0428142996583701 0.294261478119331 0.128575402635432 0.0663700407860587],...
    'String',{['Scenary ' Parameters_Texts(1,:) ': ']},...
    'FontWeight','bold',...
    'FontSize',20,...
    'FitBoxToText','off',...
    'LineStyle','none',...
    'EdgeColor',[0.831372549019608 0.815686274509804 0.784313725490196]);

% Create textbox
annotation(figure1,'textbox',...
    [0.071489263055149 0.254730713245997 0.0593440702781845 0.0375517169674651],...
    'String',{'SNR V:'},...
    'FontWeight','bold',...
    'FontSize',12,...
    'FitBoxToText','off',...
    'LineStyle','none');

% Create textbox
annotation(figure1,'textbox',...
    [0.0716222547584188 0.213973799126638 0.0717110785749146 0.0364028653171818],...
    'String',{'VAR W:'},...
    'FontWeight','bold',...
    'FontSize',12,...
    'FitBoxToText','off',...
    'LineStyle','none');

% Create textbox
annotation(figure1,'textbox',...
    [0.127102977061982 0.259097525473071 0.0812303562713518 0.0323394839718169],...
    'String',{Parameters_Texts(2,:)},...
    'FontSize',12,...
    'FitBoxToText','off',...
    'LineStyle','none');

% Create textbox
annotation(figure1,'textbox',...
    [0.127337237676916 0.219796215429403 0.0868294289897508 0.0295740397766421],...
    'String',{Parameters_Texts(3,:)},...
    'FontSize',12,...
    'FitBoxToText','off',...
    'LineStyle','none');


